    public int getRowBlanc(int[][] node) {
        int str = 0;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (node[i][j] == 0) {
                    str = i;
                    break;
                }

            }

        }
        return str;
    }

    public int getColBlanc(int[][] node) {
        int str = 0;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (node[i][j] == 0) {
                    str = j;
                    break;
                }

            }

        }
        return str;
    }
